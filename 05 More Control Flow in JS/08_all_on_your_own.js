var answer = prompt("How old are you?");

switch (answer){
	case '1':
		console.log("Wow you're already 1 year old!");
		break;
	case '13':
		console.log("First year of being a teenager!");
		break;
	case '21':
		console.log("Have a beer for me!");
		break;
	default:
		console.log("Must feel good to be ", answer, " years old!");
}